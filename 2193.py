#마지막이 0 이면 0, 1 가능
#마지막이 1 이면 0만 가능
if N < 3:
    print(1)
else:
    dp = [0] * (N + 1)
    dp[1] = 1
    dp[2] = 1
    for i in range(3, N + 1):
        dp[i] = dp[i-2] + dp[i - 1]

    print(dp[N])